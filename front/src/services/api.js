export async function fakeAccountLogin(params) {
  return axios.post('http://127.0.0.1:8000/api/login', {
    name: params.userName,
    password: params.password,
  }).then((res) => {
    if (res.status === 200) {
      return res.data;
    } else {
      message.error('登录错误,请检查用户名和密码');
    }
  });
}

